//Igualdad abstracta ==, solo compara los valores
console.log(7=='7'); //true
console.log(0 == false); //true

//Igualdad estricta ===, compara valores y tipo de datos
console.log(7==='7'); //false

//Es distinto de != , solo compara valores retorna true si son diferentes y false si son iguales
console.log(5!='5'); //false
//Es estrictamente dintinto de !==, compara valor y tipo de dato retorna true si son diferentes y false si son iguales
console.log(5!=='5');//true
